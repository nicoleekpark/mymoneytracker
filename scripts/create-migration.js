#!/usr/bin/env node
/* eslint-disable no-console */

const fs = require('fs')
const path = require('path')

const MIGRATIONS_DIR = path.join(process.cwd(), 'src', 'lib', 'db', 'migrations')
const TYPES_FILE = path.join(MIGRATIONS_DIR, 'types.ts')
const INDEX_FILE = path.join(MIGRATIONS_DIR, 'index.ts')

function pad(n) {
  return String(n).padStart(2, '0')
}

// YYYYMMDDHHmmss
function timestampId() {
  const d = new Date()
  return (
    d.getFullYear() +
    pad(d.getMonth() + 1) +
    pad(d.getDate()) +
    pad(d.getHours()) +
    pad(d.getMinutes()) +
    pad(d.getSeconds())
  )
}

function toSlug(input) {
  return input
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '_')
    .replace(/^_+|_+$/g, '')
}

function toConstName(id, slug) {
  return `m${id}_${slug}`
}

function ensureDir(p) {
  if (!fs.existsSync(p)) fs.mkdirSync(p, { recursive: true })
}

function listMigrationFiles() {
  if (!fs.existsSync(MIGRATIONS_DIR)) return []
  return fs
    .readdirSync(MIGRATIONS_DIR)
    .filter((f) => f.endsWith('.ts'))
    .filter((f) => f !== 'index.ts' && f !== 'types.ts')
}

function parseFileBase(file) {
  const base = file.replace(/\.ts$/, '')
  const m = base.match(/^(\d{14})_(.+)$/)
  if (!m) return null
  return { id: m[1], slug: m[2], base }
}

function regenIndex() {
  ensureDir(MIGRATIONS_DIR)

  if (!fs.existsSync(TYPES_FILE)) {
    // types.ts 없으면 자동 생성
    fs.writeFileSync(
      TYPES_FILE,
      `export type Migration = {
        id: number
        name: string
        up: () => void
      }`,
      'utf8'
    )
    console.log('[migrations] created src/lib/db/migrations/types.ts')
  }

  const files = listMigrationFiles()
    .map((f) => ({ f, parsed: parseFileBase(f) }))
    .filter((x) => {
      if (!x.parsed) {
        console.warn(`[migrations] skip non-standard file name: ${x.f}`)
        return false
      }
      return true
    })
    .sort((a, b) => a.parsed.id.localeCompare(b.parsed.id))

  // guards
  const ids = new Set()
  let prev = null
  for (const x of files) {
    const id = x.parsed.id
    if (ids.has(id)) {
      console.error(`[migrations] ERROR: duplicate id detected: ${id}`)
      process.exit(1)
    }
    ids.add(id)
    if (prev && id <= prev) {
      console.error(`[migrations] ERROR: non-increasing id order: ${prev} then ${id}`)
      process.exit(1)
    }
    prev = id
  }

  const imports = []
  const names = []

  for (const x of files) {
    const { id, slug, base } = x.parsed
    const constName = toConstName(id, slug)
    imports.push(`import { ${constName} } from './${base}'`)
    names.push(constName)
  }

  const content = `// AUTO-GENERATED FILE
// DO NOT EDIT BY HAND
// Generated by: node scripts/create-migration.js <name>

import type { Migration } from './types'
${imports.join('\n')}

export const MIGRATIONS: Migration[] = [
  ${names.join(',\n  ')}
]
`

  fs.writeFileSync(INDEX_FILE, content, 'utf8')
  console.log(`[migrations] regenerated index.ts (${names.length} migrations)`)
}

function createMigration(rawName) {
  if (!rawName) {
    console.error('Usage: node scripts/create-migration.js <name>')
    console.error('Example: node scripts/create-migration.js create_accounts')
    process.exit(1)
  }

  ensureDir(MIGRATIONS_DIR)

  const slug = toSlug(rawName)
  if (!slug) {
    console.error('[migrations] ERROR: invalid migration name')
    process.exit(1)
  }

  // ensure unique timestamp within same second
  let id = timestampId()
  let fileBase = `${id}_${slug}`
  let filePath = path.join(MIGRATIONS_DIR, `${fileBase}.ts`)
  if (fs.existsSync(filePath)) {
    // extremely rare, but if two migrations created in same second with same name
    // bump by 1 second
    const asNum = Number(id) + 1
    id = String(asNum)
    fileBase = `${id}_${slug}`
    filePath = path.join(MIGRATIONS_DIR, `${fileBase}.ts`)
  }

  const constName = toConstName(id, slug)

  const template = `import { execMany } from '@/lib/db/sqlite'
import type { Migration } from './types'

export const ${constName}: Migration = {
  id: ${id},
  name: '${slug}',
  up: () => {
    execMany(\`
      -- write SQL here
    \`)
  },
}
`

  fs.writeFileSync(filePath, template, 'utf8')
  console.log(`[migrations] created ${path.relative(process.cwd(), filePath)}`)

  regenIndex()
}

const args = process.argv.slice(2)
const cmd = args[0]
if (cmd === '--regen' || cmd === 'regen') {
  regenIndex()
} else {
  createMigration(args.join(' '))
}
